package cn.managesystem.domain;

public class Huiyishenqing {

  private int id;
  private String staffname;
  private String shenqingshijian;
  private String begin;
  private String huiyididian;
  private String huiyineirong;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getStaffname() {
    return staffname;
  }

  public void setStaffname(String staffname) {
    this.staffname = staffname;
  }

  public String getShenqingshijian() {
    return shenqingshijian;
  }

  public void setShenqingshijian(String shenqingshijian) {
    this.shenqingshijian = shenqingshijian;
  }

  public String getBegin() {
    return begin;
  }

  public void setBegin(String begin) {
    this.begin = begin;
  }

  public String getHuiyididian() {
    return huiyididian;
  }

  public void setHuiyididian(String huiyididian) {
    this.huiyididian = huiyididian;
  }

  public String getHuiyineirong() {
    return huiyineirong;
  }

  public void setHuiyineirong(String huiyineirong) {
    this.huiyineirong = huiyineirong;
  }

}
